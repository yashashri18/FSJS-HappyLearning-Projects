// Boolean value is either true or false.
//     - Write three JavaScript statement example which provide truthy value.
//     - Write three JavaScript statement example which provide falsy value.



//Examples of truthy values
console.log('Examples of truthy values')
console.log('Boolean true is the example of truthy value - ',Boolean(true))
console.log('(6>4) is an example of truthy value - ',Boolean(6>4))
console.log('Number 1 is an example of truthy value - ',Boolean(1))

//Output - 
// Boolean true is the example of truthy value -  true
// (6>4) is an example of truthy value -  true
// Number 1 is an example of truthy value -  true

//Examples of falsy values
console.log('Examples of falsy values')
console.log('0 is an example of falsy value - ',Boolean(Number("0")))
console.log('(8<7) is an example of falsy value - ',Boolean(8<7))
console.log('NaN is an example of falsy value - ',Boolean(NaN))

//Output 

// 0 is an example of falsy value -  false
// (8<7) is an example of falsy value -  false
// NaN is an example of falsy value -  false
