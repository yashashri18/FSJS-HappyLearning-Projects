//Math is the predefined Object in Js which has some properties and methods

//having multiple properties
console.log(Math.PI);
console.log(Math.E);

//methods

var x = 4.7
console.log(x) //4.7
console.log(Math.round(x)) //5
console.log(Math.round(4.2)) //4
console.log(Math.ceil(x)) //5
console.log(Math.floor(x)) //4



console.log(Math.floor(5.76667))//5 -excludes points

//power
console.log(Math.pow(2,4)) //16
//square root
console.log(Math.sqrt(4)) //2
//min
console.log(Math.min(3,-2,7,99,-100)) //-100
//max
console.log(Math.max(3,-2,7,99,-100)) //99


